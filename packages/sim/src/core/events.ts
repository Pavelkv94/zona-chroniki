/**
 * @module @zona/sim/core/events
 *
 * `EventBus` — append-only лог событий симуляции (задача 0.4, D-005). Это
 * ЕДИНСТВЕННЫЙ разрешённый канал общения систем помимо ECS-компонентов
 * (закон №6): системы не вызывают друг друга напрямую, а публикуют события и
 * читают чужие через `at`/`drainSince`. У каждого события есть `causedBy` —
 * ссылка на причину (или `null` для корня цепочки).
 *
 * МОДЕЛЬ ДВУХ ФАЗ (D-005, разрывает внутритиковую зависимость C-1):
 *  - `publish` кладёт событие в буфер ТЕКУЩЕГО тика; до фиксации оно НЕ видно
 *    в `log`/`at`/`drainSince`;
 *  - планировщик (0.2) в конце тика зовёт `endTick(tick)`: буфер целиком
 *    переносится в постоянный лог и очищается;
 *  - реактивные системы на тике `t` читают события прошлого тика через
 *    `at(t - 1)` — то, что уже зафиксировано, а значит одинаково для всех
 *    систем независимо от порядка их исполнения.
 *
 * ДЕТЕРМИНИЗМ (закон №8): порядок в логе = порядок вызовов `publish`. Внутри —
 * обычные массивы (буфер и лог), НИКАКОЙ итерации по Map/Set без сортировки.
 * Два прогона одной последовательности `publish` дают идентичные `id` и лог.
 *
 * МОНОТОННОСТЬ `EventId` (C-4): счётчик `eventSeq` инкрементируется на каждой
 * публикации, НЕ сбрасывается на `endTick` и сериализуется (0.5), чтобы
 * последовательность id продолжалась через save/load без коллизий. Первый
 * выданный id — `1`; `0` не используется (не путать с «нет id»). `eventSeq`
 * всегда равен количеству выданных id.
 *
 * APPEND-ONLY: неизменяемость гарантируется на ТРЁХ уровнях, чтобы `readonly`
 * (защита лишь на компиляции) нельзя было обойти в рантайме:
 *  1) событие замораживается при `publish` — И «шапка», И `payload`
 *     (`Object.freeze` на обоих; payload у нас плоский, глубина 1);
 *  2) геттер `log` отдаёт КОПИЮ массива (`slice`) — внешний `push`/`reverse`/
 *     `splice` не трогает внутренний лог и его порядок (закон №8);
 *  3) `at`/`drainSince` уже возвращают новые массивы (`filter`).
 * Прошлые записи никогда не мутируются.
 *
 * ВОССТАНОВЛЕНИЕ (seam для 0.5, по аналогии с `restoreRng`): второй аргумент
 * `init` задаёт стартовый `eventSeq` и уже накопленный `log`. Без него после
 * load id начались бы с 1 и сколлизили бы с восстановленными (C-4). Контракт:
 * `eventSeq` при восстановлении должен быть >= максимального id в `log`.
 *
 * Пример:
 * ```ts
 * const bus = createEventBus(() => world.tick);
 * const root = bus.publish({ type: 'sim/tickStarted', causedBy: null,
 *   payload: { tick: world.tick } });
 * bus.publish({ type: 'sim/snapshotTaken', causedBy: root,
 *   payload: { hash: 'ab12' } });
 * bus.endTick(world.tick);          // буфер → лог
 * bus.at(world.tick);               // оба события
 * ```
 */

import type { EventId, SimEvent, Tick } from '@zona/shared';

/**
 * Шина событий. `publish` — в буфер текущего тика; `endTick` — фиксация буфера
 * в лог; `at`/`drainSince`/`log` — чтение зафиксированного.
 */
export interface EventBus {
  /**
   * Публикует событие в буфер ТЕКУЩЕГО тика. `id` присваивается монотонно,
   * `tick` берётся из мира (через `getTick`). Возвращает выданный `id`, чтобы
   * потомки могли сослаться на него в своём `causedBy`. До `endTick` событие
   * не видно в `log`/`at`/`drainSince`.
   */
  publish<E extends SimEvent>(e: Omit<E, 'id' | 'tick'>): EventId;
  /** События, ЗАФИКСИРОВАННЫЕ на данном тике (в порядке id). */
  at(tick: Tick): readonly SimEvent[];
  /** Все зафиксированные события с `tick` включительно и позже (в порядке id). */
  drainSince(tick: Tick): readonly SimEvent[];
  /**
   * Переносит буфер текущего тика в постоянный лог и очищает буфер. Зовётся
   * планировщиком один раз в конце тика. Идемпотентен при пустом буфере
   * (повторный вызов без новых `publish` ничего не дублирует). `tick` —
   * инвариант целостности: если в буфере есть событие с иным `tick`, бросается
   * Error (ловит рассинхрон планировщика 0.2, когда `endTick` зовут не на том
   * тике, на котором публиковали).
   */
  endTick(tick: Tick): void;
  /**
   * Отбрасывает буфер ТЕКУЩЕГО тика БЕЗ коммита в лог: события, опубликованные
   * в этом тике, исчезают, лог не меняется. Планировщик зовёт `discardTick` в
   * catch, когда система бросила посреди тика, чтобы сделать тик атомарным
   * («всё или ничего»): без него недокоммиченные события повисли бы в буфере и
   * при повторе тика закоммитились бы дважды (дубль в append-only логе,
   * нарушение законов №8/№3).
   *
   * ВАЖНО про id: `eventSeq` НЕ откатывается — id уже выданных и отброшенных
   * событий «сгорают», в последовательности `EventId` появляются пропуски. Это
   * НОРМАЛЬНО и осознанно: контракт шины требует МОНОТОННОСТИ и УНИКАЛЬНОСТИ id
   * (C-4), а не их непрерывности. При повторном исполнении упавший тик получит
   * НОВЫЕ id. Восстановление после падения — отдельное выполнение, а не
   * эталонный детерминированный прогон, поэтому расхождение id с «чистым»
   * прогоном допустимо (детерминизм гарантируется для прогона без исключений).
   * Идемпотентен: на пустом буфере ничего не делает.
   */
  discardTick(): void;
  /** Весь append-only лог (КОПИЯ) в порядке возрастания id. */
  readonly log: readonly SimEvent[];
  /** Текущий счётчик выданных id (для сериализации 0.5, монотонность C-4). */
  readonly eventSeq: number;
}

/**
 * Замораживает событие целиком: сначала вложенный `payload` (глубина 1 — у нас
 * плоские payload'ы), затем саму «шапку». Порядок важен: freeze внешнего
 * объекта не делает вложенные объекты неизменяемыми, поэтому payload морозим
 * первым. Гарантирует append-only на уровне значения (D-005).
 */
function freezeEvent(ev: SimEvent): SimEvent {
  Object.freeze((ev as { payload: object }).payload);
  return Object.freeze(ev);
}

/** Точка восстановления шины для save/load (0.5, C-4). */
export interface EventBusInit {
  /** Стартовый счётчик id. Должен быть >= максимального id в `log`. */
  readonly eventSeq?: number;
  /** Уже накопленный append-only лог (события копируются в новый массив). */
  readonly log?: readonly SimEvent[];
}

/**
 * Создаёт шину событий. `getTick` возвращает текущий тик мира — шина сама не
 * хранит тик, а спрашивает его у мира на каждой публикации, поэтому смена
 * `world.tick` немедленно отражается на `tick` новых событий.
 *
 * `init` (опц.) восстанавливает шину из снапшота (0.5): `eventSeq` продолжает
 * последовательность id без коллизий (C-4), `log` — накопленную историю.
 */
export function createEventBus(getTick: () => Tick, init?: EventBusInit): EventBus {
  // Постоянный лог: только append, порядок = порядок публикаций (закон №8).
  // При восстановлении копируем вход в собственный массив, замораживая записи,
  // чтобы восстановленная история была так же неизменяема, как «живая».
  const log: SimEvent[] = init?.log
    ? init.log.map((ev) => freezeEvent(ev))
    : [];
  // Буфер событий текущего тика; переносится в лог в endTick и очищается.
  let buffer: SimEvent[] = [];
  // Монотонный счётчик выданных id. НЕ сбрасывается на endTick (C-4).
  let eventSeq = init?.eventSeq ?? 0;

  return {
    publish<E extends SimEvent>(e: Omit<E, 'id' | 'tick'>): EventId {
      const id = ++eventSeq as EventId;
      const tick = getTick();
      // Замораживаем сразу: событие неизменяемо с момента публикации
      // (append-only, D-005) — И шапка, И payload (deep freeze, глубина 1).
      const full = freezeEvent({ ...e, id, tick } as unknown as SimEvent);
      buffer.push(full);
      return id;
    },

    at(tick: Tick): readonly SimEvent[] {
      return log.filter((ev) => ev.tick === tick);
    },

    drainSince(tick: Tick): readonly SimEvent[] {
      return log.filter((ev) => ev.tick >= tick);
    },

    endTick(tick: Tick): void {
      if (buffer.length === 0) return;
      for (let i = 0; i < buffer.length; i++) {
        const ev = buffer[i] as SimEvent;
        // Инвариант целостности: буфер должен содержать только события ТЕКУЩЕГО
        // тика. Иначе планировщик рассинхронизирован (endTick не на том тике).
        if (ev.tick !== tick) {
          throw new Error(
            `EventBus.endTick(${tick}): в буфере событие с tick=${ev.tick} ` +
              `(id=${ev.id}). Планировщик зовёт endTick не на том тике.`,
          );
        }
        log.push(ev);
      }
      // Новый массив, а не .length = 0: уже отданные ссылки на буфер (если
      // кто-то их держал) не должны обнулиться задним числом.
      buffer = [];
    },

    discardTick(): void {
      // Отбрасываем буфер БЕЗ коммита: лог не меняется, eventSeq НЕ откатывается
      // (id отброшенных событий сгорают — пропуски в EventId допустимы, C-4).
      // Новый массив, а не .length = 0 — по той же причине, что в endTick.
      buffer = [];
    },

    get log(): readonly SimEvent[] {
      // Копия: внешний push/reverse/splice не должен трогать внутренний лог
      // (append-only, закон №8). Не в горячем пути — в основном сериализация 0.5.
      return log.slice();
    },

    get eventSeq(): number {
      return eventSeq;
    },
  };
}
