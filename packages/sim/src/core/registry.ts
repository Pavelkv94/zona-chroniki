/**
 * @module @zona/sim/core/registry
 *
 * Реестр SoA-компонентов — ЕДИНЫЙ источник истины для сериализации (задача 1.0,
 * D-018). `serialize`/`deserialize` итерируют реестр, чтобы детерминированно
 * (закон №8) перечислить компоненты по СТАБИЛЬНОМУ имени и прочитать/записать их
 * поля в ФИКСИРОВАННОМ порядке. Без реестра пришлось бы полагаться на порядок
 * ключей объекта-компонента и на внутренние имена bitecs — ни то, ни другое не
 * стабильно между прогонами (нарушение закона №8, C-3).
 *
 * Инварианты (проверяются `assertRegistrySorted` при загрузке модуля):
 *  - имена компонентов УНИКАЛЬНЫ и непусты (имя — ключ снапшота, коллизия = потеря);
 *  - реестр ОТСОРТИРОВАН по имени по возрастанию (детерминированный обход).
 *
 * Реестр наполняет `core/components.ts` (ПОЛНЫЙ перечень — там, это источник истины):
 * Фаза 1 (1.2/1.2b) — Position/Needs/Health/Task/Skills/Home/Animal/WorldClock + теги
 * Human/Corpse/Alive; Фаза 2 (2.1/2.8, D-046) — Settlement/AnomalyField/Job.
 * `COMPONENT_REGISTRY` реэкспортирует их отсортированный массив `DOMAIN_COMPONENTS`.
 * Разделение: registry.ts владеет КОНТРАКТОМ (тип `ComponentMeta`, инвариант сортировки,
 * точка входа сериализации), components.ts — ОБЪЯВЛЕНИЯМИ. Цикла нет: components.ts
 * тянет `ComponentMeta` type-only (стирается).
 *
 * ВАЖНО: пока в мире нет НОСИТЕЛЕЙ доменного компонента (пустой прогон ИЛИ компонент,
 * которому ни одна система/worldgen ещё не создала носителей), `serialize` пишет его
 * колонку пусто (пишутся только живые носители), поэтому добавление компонента в реестр
 * само по себе НЕ меняет голдены (пустого мира 481914ae; актуальный живой sim:100days —
 * см. cli.test/phase1-gate, растёт с добавлением событий, не компонентов).
 */

import type { ComponentRef } from './ecs';
import { DOMAIN_COMPONENTS } from './components';

/**
 * Описание одного компонента для сериализации. `name` — стабильный ключ снапшота
 * (не зависит от имени переменной/порядка объявления). `ref` — сам SoA-компонент
 * (объект-хранилище из `defineComponentT`). `fields` — ФИКСИРОВАННЫЙ порядок имён
 * полей: и запись, и чтение снапшота идут строго по нему, поэтому порядок нельзя
 * менять без роста версии формата (иначе старые снапшоты прочитаются криво).
 */
export interface ComponentMeta {
  /** Стабильный ключ снапшота. Уникален в реестре. */
  readonly name: string;
  /** SoA-компонент (bitecs `ComponentRef` = объект-хранилище с колонками). */
  readonly ref: ComponentRef;
  /** Фиксированный порядок полей: читаются/пишутся строго в нём. */
  readonly fields: readonly string[];
}

/**
 * Глобальный реестр компонентов симуляции. Отсортирован по `name` (закон №8).
 * Наполнен доменными компонентами 1.2 (`DOMAIN_COMPONENTS`, core/components.ts,
 * D-019). `serialize`/`deserialize` берут его по умолчанию; тесты, которым нужна
 * изоляция от прод-компонентов, передают собственный тест-реестр параметром.
 */
export const COMPONENT_REGISTRY: readonly ComponentMeta[] = DOMAIN_COMPONENTS;

/**
 * Проверяет инварианты реестра: непустые УНИКАЛЬНЫЕ имена, отсортированные по
 * возрастанию (UTF-16). Бросает `Error` при нарушении — так мис-конфигурация 1.2
 * (дубль имени / нарушенный порядок) падает при загрузке модуля, а не молчаливо
 * ломает детерминизм снапшота. Идемпотентна и чиста (без побочных эффектов).
 */
export function assertRegistrySorted(reg: readonly ComponentMeta[]): void {
  for (let i = 0; i < reg.length; i++) {
    const meta = reg[i] as ComponentMeta;
    if (meta.name.length === 0) {
      throw new Error(`assertRegistrySorted: пустое имя компонента (индекс ${i}).`);
    }
    if (i > 0) {
      const prev = (reg[i - 1] as ComponentMeta).name;
      if (prev === meta.name) {
        throw new Error(
          `assertRegistrySorted: дублирующееся имя компонента "${meta.name}".`,
        );
      }
      if (prev > meta.name) {
        throw new Error(
          `assertRegistrySorted: реестр не отсортирован по имени: "${prev}" перед "${meta.name}".`,
        );
      }
    }
  }
}

// Инвариант глобального реестра проверяется при загрузке модуля (в 1.0 — пустой,
// проходит тривиально; 1.2 получит ранний throw на кривом наполнении).
assertRegistrySorted(COMPONENT_REGISTRY);
