/**
 * @module @zona/sim/balance/social
 *
 * Балансовые константы ПАМЯТИ / ОТНОШЕНИЙ / ОБХОДА (задача 2.15, D-050/D-058, закон
 * №7: пороги/ставки затухания живут ТОЛЬКО здесь, а не магическими числами в системе
 * MemoryDecay). Значения СТАРТОВЫЕ; их тюнингует balance-analyst по метрикам длинного
 * headless-прогона (как долго NPC помнит обиду, как быстро остывает вражда).
 *
 * ── Затухание — ДЕТЕРМИНИРОВАННАЯ функция времени (закон №2, НЕ «X% забыть») ────
 * Память и отношения слабеют потому, что событие УХОДИТ В ПРОШЛОЕ (нет подкрепления) —
 * это причинный процесс «среды сознания» (как рост нужд Needs или накопление заряда
 * поля), а не бросок кости. Ставки заданы «за тик»; система MemoryDecay идёт с редким
 * шагом `every: MEMORYDECAY_CADENCE` и за один вызов применяет `ставка × cadence`
 * (компенсация редкого шага, как ставки пастьбы Animals «×every» / заряд ArtifactSpawn).
 *
 * ── Горизонт памяти ~60 дней ──────────────────────────────────────────────────
 * Свежая (salience=1) память полностью выветривается примерно за `MEMORY_MAX_AGE_DAYS`
 * игровых суток — тогда её salience достигает порога забвения, а страховочный prune по
 * ВОЗРАСТУ гарантирует удаление даже подкреплённых-и-брошенных записей. Отношение силой
 * 1.0 за тот же срок остывает к нейтралу (0).
 */

import { TICKS_PER_DAY } from './time';

// ── Горизонт памяти ──────────────────────────────────────────────────────────

/**
 * Горизонт памяти в игровых СУТКАХ. За этот срок свежая память выветривается, а
 * подкреплённая-и-заброшенная запись форсированно удаляется по возрасту (страховка от
 * «вечной» памяти при разовом сильном подкреплении). ~60 дней = сезонная память NPC.
 */
export const MEMORY_MAX_AGE_DAYS = 60;

/**
 * Горизонт памяти в ТИКАХ (`MEMORY_MAX_AGE_DAYS × TICKS_PER_DAY`). MemoryDecay удаляет
 * запись, если `tick − record.tick > MEMORY_MAX_AGE_TICKS` (страховочный prune по
 * возрасту, независимо от salience). Также база ставок затухания ниже.
 */
export const MEMORY_MAX_AGE_TICKS = MEMORY_MAX_AGE_DAYS * TICKS_PER_DAY;

/**
 * Порог ЗАБВЕНИЯ salience. Запись памяти выбрасывается, когда её `salience` падает ниже
 * этого значения (память истёрлась) ИЛИ она старше `MEMORY_MAX_AGE_TICKS`. `> 0`, чтобы
 * почти-нулевые записи не копились балластом; малое значение = память живёт почти до
 * конца горизонта.
 */
export const MEMORY_FORGET_THRESHOLD = 0.05;

/**
 * Убыль salience памяти ЗА ТИК. За один вызов MemoryDecay (`every: cadence`) salience
 * уменьшается на `MEMORY_SALIENCE_DECAY_PER_TICK × cadence`. Нормирована так, что
 * salience=1 достигает 0 ровно за горизонт (`1 / MEMORY_MAX_AGE_TICKS`), то есть падает
 * ниже порога забвения незадолго до страховочного prune по возрасту. Чистая арифметика,
 * БЕЗ rng (закон №2), детерминирована и resume-safe (зависит только от salience/tick записи).
 */
export const MEMORY_SALIENCE_DECAY_PER_TICK = 1 / MEMORY_MAX_AGE_TICKS;

// ── Отношения ─────────────────────────────────────────────────────────────────

/** Нижняя граница отношения (враг). Отношение клампится в [`RELATION_MIN`..`RELATION_MAX`]. */
export const RELATION_MIN = -1;

/** Верхняя граница отношения (союзник). */
export const RELATION_MAX = 1;

/**
 * Убыль МОДУЛЯ отношения ЗА ТИК — движение к нейтралу (0). За вызов MemoryDecay значение
 * подтягивается к 0 на `RELATION_DECAY_PER_TICK × cadence` (в сторону уменьшения модуля,
 * не переваливая через 0). Тот же горизонт, что и память: отношение силой 1.0 остывает к
 * нейтралу за ~60 дней без подкрепляющих событий памяти. Детерминировано, БЕЗ rng.
 */
export const RELATION_DECAY_PER_TICK = 1 / MEMORY_MAX_AGE_TICKS;

/**
 * Эпсилон НЕЙТРАЛА отношения. Когда модуль отношения падает до/ниже этого значения, оно
 * считается нейтральным (0) и запись ВЫБРАСЫВАЕТСЯ (нейтрал = отсутствие записи по
 * умолчанию, массив держит только ненулевые отношения). `> 0`, чтобы почти-нулевые
 * отношения не копились балластом.
 */
export const RELATION_NEUTRAL_EPSILON = 0.02;

// ── Ставка затравки памяти (общий якорь для будущих 2.12/2.13) ───────────────

/**
 * Стартовая salience только что записанной памяти (по умолчанию в `addMemory`, если
 * вызывающий её не задал). Свежая память яркая — `= RELATION_MAX`-масштаб (1.0). Хелпер
 * клампит переданное значение в [0..1]. Держится здесь (а не в системе), чтобы 2.12/2.13
 * тюнинговали силу разных видов памяти из одного места (закон №7).
 */
export const MEMORY_INITIAL_SALIENCE = 1;
