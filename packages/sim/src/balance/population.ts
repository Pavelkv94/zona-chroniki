/**
 * @module @zona/sim/balance/population
 *
 * Балансовые константы ПРИЧИННОГО притока населения (задача 2.14, система
 * PopulationInflux, D-061, закон №7: веса/пороги/окно живут ТОЛЬКО здесь, а не
 * магическими числами в системе). Значения СТАРТОВЫЕ; их тюнингует balance-analyst
 * по метрикам длинного headless-прогона (мир не должен ни коллапсировать в
 * вымирание — D-043, ни взрываться перенаселением за 100 дней).
 *
 * ── Модель (закон №2: причинность, НЕ «X% спавн/тик») ────────────────────────
 * Приток — это ПОРОГ по СОСТОЯНИЮ мира, а не бросок кости над фактом появления.
 * Раз в `INFLUX_CADENCE` тиков система читает ОКНО последних `INFLUX_WINDOW_TICKS`
 * тиков лога событий и складывает ДЕТЕРМИНИРОВАННУЮ «привлекательность Зоны»:
 *
 *   attractiveness =
 *       W_ARTIFACT_SPAWNED  · (родилось артефактов, `artifact/spawned`)
 *     + W_ARTIFACT_COLLECTED· (подобрано артефактов, `artifact/collected`)
 *     + W_EXPORT            · (экспортных сделок за Периметр, `item/exported`)
 *     − W_DEATH             · (смертей, `entity/died`)
 *     − W_BANDITRY          · (грабёжных стычек человек-vs-человек, `encounter/started`)
 *
 * Притягивающие слагаемые (нашли ценный хабар/артефакты, кто-то разбогател и вывез
 * добро за Периметр) поднимают привлекательность; отталкивающие (волны смертей,
 * бандитизм) — опускают. При `attractiveness >= INFLUX_THRESHOLD` из-за Периметра
 * приходит ГРУППА `[GROUP_MIN, GROUP_MAX]` новичков (закон №1: точка входа Кордон,
 * НЕ «возле игрока»). Размер группы — категория «генерация мира» (D-021, seeded rng
 * допустим, как размер стада worldgen), сам ПОРОГ и привлекательность — чистая
 * арифметика над логом (rng на факт притока НЕ влияет).
 *
 * ── Окно == шаг (каждое событие учитывается РОВНО ОДИН раз) ───────────────────
 * `INFLUX_WINDOW_TICKS === INFLUX_CADENCE`: последовательные оценки смотрят на
 * непересекающиеся, стыкующиеся отрезки лога `[t−N, t−1]`, `[t, t+N−1]`, … —
 * значит одно событие входит ровно в одну оценку и не «двоится» в притоке
 * (иначе один яркий артефакт тянул бы новичков каждый шаг → взрыв населения).
 * Guard-канарейка в системе падает, если это равенство нарушат перебалансом.
 */

/**
 * Шаг планировщика PopulationInflux (тиков). Приток — редкое демографическое
 * событие, по-тиковой реакции не требует; редкий шаг дешевле (бюджет D-006).
 * Совпадает с окном (см. `INFLUX_WINDOW_TICKS`): каждое событие лога попадает
 * ровно в одну оценку притока. `240` тиков ≈ 4 игровых часа (TICKS_PER_DAY=1440):
 * шесть оценок притока в сутки. Структурная величина (не баланс/не контент),
 * поэтому число здесь; окно к нему привязано равенством.
 */
export const INFLUX_CADENCE = 240;

/**
 * Длина окна лога для расчёта привлекательности (тиков). РАВНА шагу
 * `INFLUX_CADENCE` — так оценки непересекающиеся и каждое событие учитывается
 * ровно один раз (см. docblock: без этого один артефакт множил бы приток). Держите
 * равенство при тюнинге (guard в системе бросит при рассинхроне).
 */
export const INFLUX_WINDOW_TICKS = 240;

// ── Веса слагаемых привлекательности (безразмерные «очки», закон №7) ──────────

/**
 * Вес РОЖДЕНИЯ артефакта в поле (`artifact/spawned`) — притягивающий (+). Слух о
 * «выброшенном полем» ценном хабаре манит одиночек в Зону. Достаточно веский, чтобы
 * несколько находок за окно перевесили порог, но не одиночная находка (тюнинг).
 */
export const W_ARTIFACT_SPAWNED = 3;

/**
 * Вес ПОДБОРА артефакта NPC (`artifact/collected`) — притягивающий (+). Кто-то уже
 * поднял артефакт (значит хабар реально выносим) — сильнейший магнит слухов.
 */
export const W_ARTIFACT_COLLECTED = 4;

/**
 * Вес ЭКСПОРТНОЙ сделки за Периметр (`item/exported`) — притягивающий (+). «Богатый
 * вышел с добром» (D-055: экспорт — единственный money-faucet): доказательство, что
 * в Зоне можно подняться, притягивает новых искателей.
 */
export const W_EXPORT = 2;

/**
 * Вес СМЕРТИ (`entity/died`) — отталкивающий (−). Волна смертей (голод, звери, бои)
 * отпугивает: Зона выглядит смертельной. Умеренный, чтобы естественная убыль не
 * закрывала приток совсем (иначе спираль смерти D-043 не размыкается), но заметные
 * волны гибели тормозили поток.
 */
export const W_DEATH = 2;

/**
 * Вес БАНДИТСКОЙ стычки человек-vs-человек (`encounter/started` с людьми по ОБЕ
 * стороны) — отталкивающий (−). Грабежи на входе отпугивают сильнее «естественной»
 * смерти: беспредел = никто не хочет соваться. SEAM: пока (до цепочки бандитов
 * 2.11–2.13 в конвейере) человек-vs-человек стычек в мире нет — слагаемое = 0 без
 * спец-кода (охота человек-vs-зверь под критерий «люди по обе стороны» не подпадает).
 */
export const W_BANDITRY = 3;

/**
 * ПОРОГ привлекательности, при достижении которого (`>=`) приходит группа новичков.
 * Калибр: несколько притягивающих событий за окно (напр. пара находок артефактов
 * или горсть экспортов) перевешивают порог, если их не «съели» смерти/бандитизм.
 * Мир без активности (ни хабара, ни экспорта) НЕ притягивает никого (attractiveness
 * ~0 < порог) — приток строго заслужен состоянием мира, а не идёт «фоном» (закон №2).
 */
export const INFLUX_THRESHOLD = 6;

/** Минимальный размер группы новичков при срабатывании порога (включительно). */
export const GROUP_MIN = 1;

/** Максимальный размер группы новичков при срабатывании порога (включительно). */
export const GROUP_MAX = 3;
