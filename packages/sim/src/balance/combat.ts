/**
 * @module @zona/sim/balance/combat
 *
 * Балансовые константы резолвера столкновений (закон №7; см. D-022: единый
 * Encounter Resolver над Combatant[][], животное = комбатант с ammo=0/melee>0).
 * Резолвер — чистая функция; его ЕДИНСТВЕННЫЙ источник случайности — разброс
 * попадания через seeded RNG системы (закон №2: не «X% шанс события», а разброс
 * физиологии выстрела). Значения стартовые, тюнит balance-analyst.
 */

/**
 * Максимум раундов обмена в одном столкновении. Ограничивает стоимость
 * резолвера (бюджет тика, D-006) и не даёт бесконечной перестрелке — стороны
 * расходятся/гибнут за конечное число раундов.
 */
export const MAX_ROUNDS = 6;

/** Базовая точность выстрела до модификаторов (доля попадания 0..1). */
export const BASE_ACCURACY = 0.6;

/**
 * ЗАРЕЗЕРВИРОВАНО (пока НЕ используется резолвером 1.10b). Множитель точности от
 * укрытия цели: итог = base × (1 - cover×COVER_MULT). У `Combatant` (D-022) поля
 * cover нет — укрытие подключит будущая задача (человек-vs-человек в укрытии),
 * тогда эта константа станет активной. Держим как готовый параметр из 1.1.
 */
export const COVER_ACCURACY_MULT = 0.5;

/**
 * Прибавка к точности от СИЛЫ бойца: итог += powerNorm × SKILL_MULT, где
 * `powerNorm = clamp(power / POWER_ACCURACY_REF, 0, 1)` (резолвер 1.10b). `power`
 * уже вбирает навык стрелка (человек: shooting×оружие) или силу вида (species.power),
 * поэтому нормируется единым референсом — без хардкода вида (D-022). Сильнее боец —
 * надёжнее попадание.
 */
export const SKILL_ACCURACY_MULT = 0.3;

/** Расход патронов на один раунд стрельбы (списывается из инвентаря, закон №3). */
export const AMMO_PER_ROUND = 1;

/**
 * Базовый урон попадания из огнестрела (единиц здоровья). Melee-урон берётся из
 * species.json (melee) для животного и из `HUMAN_UNARMED_MELEE` для безоружного
 * человека (комбатант несёт своё поле `melee`).
 */
export const RANGED_HIT_DAMAGE = 25;

/**
 * Базовая точность БЛИЖНЕГО боя (доля-«конверт разброса» до бонуса силы). Выше
 * стрелковой `BASE_ACCURACY`: в упор удар/укус приходится надёжнее выстрела, но
 * промах всё равно возможен (разброс телесного контакта — та же физиология, №2).
 */
export const MELEE_BASE_ACCURACY = 0.8;

/**
 * Референс силы (`Combatant.power`), дающий ПОЛНЫЙ бонус к точности
 * (`SKILL_ACCURACY_MULT`). Нормировка: `powerNorm = clamp(power / REF, 0, 1)`,
 * затем `acc += powerNorm × SKILL_ACCURACY_MULT`. Так и человек (power из навыка×
 * оружия), и зверь (power из species.json) кладутся в единый [0..1] без хардкода
 * вида в резолвере (D-022). Значение стартовое — тюнингует balance-analyst.
 */
export const POWER_ACCURACY_REF = 10;

/**
 * Порог МОРАЛИ: доля потерь стороны, при достижении которой сторона ЛОМАЕТСЯ
 * (бежит) — детерминированно от СОСТОЯНИЯ боя, НЕ «X% побега» (закон №2). Считается
 * как `casualties / initialCount >= MORALE_BREAK_FRACTION`. Сторона из одного
 * бойца никогда не «ломается» живой (0 потерь), а её гибель — это ВЫБЫВАНИЕ
 * (обрабатывается раньше морали), поэтому одиночная охота решается элиминацией.
 */
export const MORALE_BREAK_FRACTION = 0.5;

/**
 * Множитель силы человека-стрелка: `power = shooting × HUMAN_WEAPON_POWER` (при
 * наличии совместимого оружия+патронов). Навык `shooting ∈ [SKILL_MIN..SKILL_MAX]`
 * (worldgen) даёт power ~1.5..7.5 — сопоставимо с олень(2)/кабан(8) из species.json.
 * Безоружный человек стреляет power=0 и опирается на `HUMAN_UNARMED_MELEE`.
 */
export const HUMAN_WEAPON_POWER = 10;

/** Урон безоружного удара человека (`Combatant.melee` для людей; аналог species.melee). */
export const HUMAN_UNARMED_MELEE = 5;

/** Нижняя граница итоговой точности после всех модификаторов (всегда есть шанс). */
export const MIN_ACCURACY = 0.05;

/** Верхняя граница итоговой точности (промах возможен всегда). */
export const MAX_ACCURACY = 0.95;
