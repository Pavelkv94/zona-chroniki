/**
 * @module @zona/sim/balance/needs
 *
 * Балансовые константы физиологии NPC (закон №7: ставки/пороги нужд живут ТОЛЬКО
 * здесь, не как магические числа в системе Needs). Модель: 1 тик = 1 минута
 * (см. balance/time). Все нужды в шкале 0..100 (0 = сыт/бодр, 100 = предел),
 * здоровье в шкале 0..100 (0 = смерть). Значения — разумные стартовые; их
 * тюнингует balance-analyst по метрикам 100-дневного прогона.
 *
 * Обоснование ставок исходит из «сколько тиков до критического уровня»:
 *  - жажда острее голода (обезвоживание убивает быстрее),
 *  - усталость копится за световой день бодрствования,
 *  - урон от истощения мал, но неотвратим — смерть эмерджентна, не по таймеру.
 */

// ── Ставки роста нужд (единиц шкалы за тик) ──────────────────────────────────

/** Голод: ~0.035/тик ⇒ полная шкала за ~2 суток без еды. */
export const HUNGER_PER_TICK = 0.035;

/** Жажда: ~0.07/тик ⇒ полная шкала за ~1 сутки без воды (острее голода). */
export const THIRST_PER_TICK = 0.07;

/** Усталость бодрствования: ~0.10/тик ⇒ предел за ~16 ч на ногах. */
export const FATIGUE_PER_TICK = 0.1;

/**
 * Затухание страха: ~0.5/тик ⇒ пик страха (100) угасает к 0 за ~200 тиков
 * (~3.3 игровых часа без новой угрозы). Страх — единственная нужда, что со
 * временем УБЫВАЕТ (адреналин спадает): его РОСТ порождает Perception (1.7) от
 * восприятия угрозы, а система Needs (1.5) лишь затухает его к 0. Значение —
 * стартовое, тюнингует balance-analyst.
 */
export const FEAR_DECAY_PER_TICK = 0.5;

/**
 * Подъём страха за тик, пока рядом (co-located) УГРОЗА — агрессивное животное
 * непугливого вида (кабан, `species.flees === false`). Публикует его Perception
 * (1.7) из состояния мира (закон №2 — не «X% испугаться», а «угроза физически
 * рядом»). Ставка ЗАВЕДОМО ВЫШЕ `FEAR_DECAY_PER_TICK` (0.5): пока угроза рядом,
 * рост обгоняет затухание Needs (1.5) и страх копится к потолку; как только
 * угроза ушла — остаётся только затухание, и страх спадает. Так «страшно, пока
 * зверь рядом» рождается эмерджентно из двух ставок, без флага «в страхе».
 * Значение стартовое — тюнингует balance-analyst по метрикам прогона.
 */
export const FEAR_FROM_THREAT_PER_TICK = 3.0;

// ── Критические пороги (выше → урон истощения) ───────────────────────────────

/** Порог голода, за которым начинается урон голодания. */
export const HUNGER_CRITICAL = 80;

/** Порог жажды, за которым начинается урон обезвоживания. */
export const THIRST_CRITICAL = 85;

/** Порог усталости, за которым NPC теряет эффективность/начинает валиться. */
export const FATIGUE_CRITICAL = 90;

// ── Урон истощения (единиц здоровья за тик выше порога) ──────────────────────

/** Урон голодания: медленное угасание (~смерть за ~3.5 суток за порогом). */
export const STARVATION_DAMAGE_PER_TICK = 0.02;

/** Урон обезвоживания: вдвое быстрее голодания. */
export const DEHYDRATION_DAMAGE_PER_TICK = 0.04;

// ── Восстановление ───────────────────────────────────────────────────────────

/**
 * Восстановление усталости за тик сна ДОМА (~полный отдых за ~11 ч непрерывного
 * сна; фактически медленнее — Needs (1.5) параллельно растит fatigue). Это ставка
 * задачи SLEEP (TaskEffects 1.8e), эффективная ТОЛЬКО когда сущность в своём Home.
 * Голод восстанавливается НЕ ставкой, а разово через nutrition съеденного предмета
 * (items.json) — источник физически расходуется из инвентаря (закон №3).
 */
export const SLEEP_RECOVERY_PER_TICK = 0.15;

/**
 * Восстановление усталости за тик ОТДЫХА (задача REST, TaskEffects 1.8e) —
 * отдых у костра/на привале В ЛЮБОЙ локации, без нужды в доме. СОЗНАТЕЛЬНО СЛАБЕЕ
 * сна (`< SLEEP_RECOVERY_PER_TICK`): привал восстанавливает меньше, чем полноценный
 * сон в укрытии дома. ~полный отдых за ~28 ч чистого отдыха (реально — фон для
 * fallback-задачи D-020, не основной способ выспаться). Значение стартовое —
 * тюнингует balance-analyst.
 */
export const REST_RECOVERY_PER_TICK = 0.06;

/**
 * Восстановление жажды за тик ПИТЬЯ (задача DRINK, TaskEffects 1.8e) у источника
 * воды (loc.water===true). Вода берётся из СРЕДЫ локации (река/колодец), НЕ из
 * инвентаря — предмет не создаётся и не расходуется (закон №3 не нарушается: пьёт
 * из источника). Крупная ставка: полная жажда (100) закрывается за ~5 тиков-минут
 * (напиться быстро — акт секундный против сна). Значение стартовое — тюнингует
 * balance-analyst.
 */
export const DRINK_RECOVERY_PER_TICK = 20;

/**
 * БАЗОВАЯ ставка восстановления голода за тик СОБИРАТЕЛЬСТВА (задача FORAGE,
 * TaskEffects 1.8e). Фактическое восстановление = `FORAGE_RECOVERY_PER_TICK *
 * loc.forage` (обилие подножного корма локации, 0..1): в богатых угодьях (forage
 * 0.7) даёт ~0.35/тик, в бедных (Саркофаг, forage 0.05) — ~0.025/тик (почти ноль,
 * как и должно быть в мёртвой зоне). Собранная еда — из СРЕДЫ (найдена/подобрана),
 * съедается на месте и в инвентарь не кладётся (закон №3 не нарушается: источник —
 * подножный корм локации, а не «из воздуха»). Ставка заведомо скромнее разового
 * EAT (nutrition 25–45): FORAGE — медленный fallback (D-020), а не сытный обед.
 * Значение стартовое — тюнингует balance-analyst.
 */
export const FORAGE_RECOVERY_PER_TICK = 0.5;

/**
 * Регенерация здоровья за тик, когда все нужды ниже критических порогов.
 * Мала: раны заживают, но истощение всегда обгоняет лечение без ухода.
 */
export const HEALTH_REGEN_PER_TICK = 0.01;

/** Стартовое/максимальное здоровье NPC. */
export const HEALTH_MAX = 100;

/** Максимум любой нужды (потолок шкалы). */
export const NEED_MAX = 100;
