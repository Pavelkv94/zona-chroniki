/**
 * @module @zona/sim/balance/economy
 *
 * Балансовые константы экономики поселений (задача 2.3, закон №7: никаких магических
 * чисел в системе Economy — только отсюда). Система `systems/economy.ts` читает эти
 * ставки; сами РЕЦЕПТЫ/ПОТРЕБЛЕНИЕ/ОЧЕРЕДЬ СТРОЙКИ — КОНТЕНТ в data/settlements.json
 * (закон №10), здесь — лишь скорости/пороги, общие для всех поселений (тюнит
 * balance-analyst длинными прогонами). Всё детерминировано (закон №2/№8): ни одна
 * ставка не «шанс», а множитель/порог, применяемый к состоянию мира.
 *
 * ── КАДЕНС И ДОЛЯ ДНЯ ─────────────────────────────────────────────────────────
 * Economy исполняется каждые `ECONOMY_CADENCE` тиков. Подушевое потребление задано
 * «в день» (data.perCapita), поэтому за один запуск списывается доля дня
 * `ECONOMY_CADENCE / TICKS_PER_DAY`. Дробный остаток НЕ теряется и НЕ хранится
 * во «внеигровом» накопителе: он копится в СЕРИАЛИЗУЕМОМ поле долга склада
 * (`consumptionDebt` в ResourceStore поселения), а из склада списываются только
 * ЦЕЛЫЕ единицы (закон №3 — предметы дискретны; массовый инвариант держится точно
 * на целых, без плавающей ошибки). См. docblock systems/economy.ts.
 *
 * ── МОРАЛЬ ───────────────────────────────────────────────────────────────────
 * Мораль 0..1 (Settlement.morale). Дефицитный запуск (склад не покрыл спрос) роняет
 * её на `MORALE_DEFICIT_DROP`; достаточный — поднимает к `MORALE_MAX` на
 * `MORALE_RECOVER`. Ниже/на `MORALE_ABANDON_THRESHOLD` поселение заброшено. Эти три
 * числа задают «сколько дней затяжного голода до распада» — стартовые, тюнятся.
 */

/**
 * Каденс системы Economy (тиков между запусками). Совпадает с `schedule.every`
 * системы. Подушевое потребление (за день) домножается на долю дня
 * `ECONOMY_CADENCE / TICKS_PER_DAY`. 10 тиков = 10 игровых минут: экономика поселения
 * не требует по-тиковой реакции (в отличие от боя), но должна списывать провизию
 * достаточно часто, чтобы дефицит проявлялся плавно, а не скачком раз в сутки.
 */
export const ECONOMY_CADENCE = 10;

// ── Мораль (Settlement.morale, шкала 0..1) ───────────────────────────────────

/** Потолок морали: достаток поднимает мораль не выше этого (== стартовому максимуму). */
export const MORALE_MAX = 1.0;

/** Пол морали: ниже нуля не опускается (кламп). */
export const MORALE_MIN = 0.0;

/**
 * На сколько падает мораль за ОДИН дефицитный запуск (склад не покрыл спрос на
 * провизию). Определяет крутизну спада при голоде: чем больше, тем быстрее
 * поселение распадётся под затяжным дефицитом. Стартовое значение.
 */
export const MORALE_DEFICIT_DROP = 0.01;

/**
 * На сколько растёт мораль за ОДИН достаточный запуск (склад покрыл спрос). Меньше
 * `MORALE_DEFICIT_DROP` (восстановление медленнее просадки: доверие теряется быстрее,
 * чем возвращается). Стартовое значение.
 */
export const MORALE_RECOVER = 0.003;

/**
 * Порог заброшенности: как только мораль просела до/ниже него, поселение покидается
 * (`settlement/abandoned`). Не 0, а небольшое положительное: жители уходят ДО полного
 * отчаяния. «Затяжной дефицит» кодируется естественно — мораль пробивает порог лишь
 * после многих дефицитных запусков подряд (стартовые `MORALE_DEFICIT_DROP` × число
 * запусков), а не по отдельному счётчику дней (счётчик хранить негде — resume-safe
 * через сериализуемую мораль).
 */
export const MORALE_ABANDON_THRESHOLD = 0.15;

// ── Защищённость (Settlement.security, шкала 0..1) — ПРОИЗВОДНАЯ от населения ───

/**
 * Вклад одного жителя в защищённость поселения (гарнизон растёт с населением).
 * `security` в 2.3 — ПРОИЗВОДНАЯ величина: `clamp(pop × SECURITY_PER_CAPITA, 0,
 * SECURITY_MAX)` (упрощённо, D-046; влияние на грабёж — задача 2.12). Детерминирована
 * от населения, поэтому resume-safe без хранимого состояния.
 */
export const SECURITY_PER_CAPITA = 0.05;

/** Потолок производной защищённости (даже крупное поселение не «неприступно»). */
export const SECURITY_MAX = 0.95;

// ── Строительство (Settlement.buildProgress, шкала 0..1) ──────────────────────

/**
 * Прирост прогресса стройки за ОДИН запуск на ОДНОГО работника (носитель
 * `Job.employer === поселение`). Стройка идёт ТОЛЬКО при наличии труда (в 2.3 труд
 * назначает 2.4 — без работников прогресс не растёт; тесты навешивают Job вручную).
 * `buildProgress += BUILD_PROGRESS_PER_WORKER × workers`, при достижении 1.0 —
 * `settlement/built` и следующий проект из buildQueue. Стартовое значение.
 */
export const BUILD_PROGRESS_PER_WORKER = 0.01;
