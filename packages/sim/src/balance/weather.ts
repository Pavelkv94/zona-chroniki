/**
 * @module @zona/sim/balance/weather
 *
 * Балансовые константы погоды и суточного цикла (закон №7). Порог дня/ночи
 * (DAWN/DUSK) задаётся здесь, а сам признак «ночь» НЕ хранится — выводится из
 * `tick % TICKS_PER_DAY` (D-019, закон №8: не дублируем детерминированную
 * функцию времени состоянием). Погода, напротив, хранится в WorldClock
 * (длительность + подпоток rng), т.к. это марковский процесс с памятью.
 *
 * Времена — в тиках-минутах (1 тик = 1 минута, balance/time). Значения
 * стартовые, тюнит balance-analyst.
 */

/** Типы погоды. Влияют на видимость/движение/выбросы (ecosystem-engineer). */
export const WEATHER_TYPES = ['clear', 'overcast', 'rain', 'fog', 'storm'] as const;

/** Тип идентификатора погоды. */
export type WeatherType = (typeof WEATHER_TYPES)[number];

/** Минимальная длительность одного погодного состояния (тиков ≈ 2 ч). */
export const WEATHER_MIN_DURATION = 120;

/** Максимальная длительность одного погодного состояния (тиков ≈ 12 ч). */
export const WEATHER_MAX_DURATION = 720;

// ── Суточный цикл ────────────────────────────────────────────────────────────

/**
 * Рассвет: тик от начала суток, с которого считается день. 6:00 → 6×60=360.
 * `isNight(t) = (t % TICKS_PER_DAY) < DAWN_TICK || (t % TICKS_PER_DAY) >= DUSK_TICK`.
 */
export const DAWN_TICK = 360;

/** Закат: тик от начала суток, с которого считается ночь. 21:00 → 21×60=1260. */
export const DUSK_TICK = 1260;
