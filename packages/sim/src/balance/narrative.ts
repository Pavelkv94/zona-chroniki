/**
 * @module @zona/sim/balance/narrative
 *
 * Балансовые константы НАРРАТИВНОГО ХРЕБТА Фазы 3 (задача 3.1, D-067, закон №7): БАЗОВЫЕ
 * ВЕСА ЗНАЧИМОСТИ по ТИПУ события + параметры масштабирования по известности (`fame`).
 * Все магические числа значимости живут ТОЛЬКО здесь — чистая функция
 * `narrative/significance.ts` их лишь читает (не хардкодит). Значения СТАРТОВЫЕ; их
 * тюнингует balance-analyst по метрикам «интересности эфира» длинного прогона (сколько
 * записей в летописи за 100 дней, не заваливает ли рутина драму).
 *
 * ── ЗАЧЕМ ВЕСА (GDD §10.2) ────────────────────────────────────────────────────
 * Значимость события — DERIVED число [0..1]: насколько событие достойно попасть в
 * летопись / эфир. Оно НЕ случайно (закон №2): выводится из ТИПА события и известности
 * его участников. GDD §10.2 задаёт три опорные точки, вокруг которых построена вся шкала:
 *   • заброшенность/гибель ПОСЕЛЕНИЯ  = МАКСИМУМ (1.0) — крупнейшее событие мира;
 *   • смерть NPC                      = вес ИЗВЕСТНОСТИ жертвы (аноним ниже, легенда выше);
 *   • редкий артефакт (по tier)       = СРЕДНЕ (растёт со ступенью).
 * Остальные типы расставлены между этими якорями по «драматургическому весу»: конфликт и
 * смерть тяжелее рутины (движение, торговля, восприятие, леджер массы). Рутинные шумовые
 * события (перемещение, выбор задачи, тик) дают ~0 — они не должны засорять летопись.
 *
 * ── ПОЧЕМУ fame — ХРАНИМЫЙ аккумулятор, а не скан лога ────────────────────────
 * Известность растёт МОНОТОННО по мере упоминаний сущности в летописи/эфире (её позовут
 * Chronicle 3.2 / Radio 3.5 каузальным шагом «упомянут → fame↑»). Держать её выводимой из
 * лога = скан всего лога на КАЖДУЮ оценку значимости = O(лог) на событие = квадрат за
 * прогон (ломает бюджет D-006). Поэтому `fame` — ResourceStore-ключ `'fame'` (число на eid,
 * автосериализуется как money/memory, D-050/D-007). CAP не даёт известности расти без границ
 * (иначе одна легенда переклампит всю шкалу в 1.0). Спад (decay) отложен в задачу 3b.
 */

// ── Летопись (Chronicle 3.2, D-068): порог записи + петля известности ──────────

/**
 * ПОРОГ ЗНАЧИМОСТИ летописи (задача 3.2, D-068, закон №7). Событие прошлого тика попадает в
 * летопись (`chronicle/recorded`), только если `significance(ev, world) >= CHRONICLE_THRESHOLD`.
 * Отсекает рутину и оставляет ДРАМУ: при `0.4` записываются смерть NPC (base 0.48), разрешение
 * столкновения (0.42 + бонус за потери), завершение/гибель поселения (0.55/1.0), но НЕ грабёж
 * (0.28), завязка стычки (0.30), торговля/восприятие/леджер (≤0.12). Порог ПРИЧИННЫЙ (закон №2):
 * значимость выводится из состояния мира, без rng. Стартовое значение; тюнингует balance-analyst
 * по «плотности летописи» длинного прогона (сколько записей за 100 дней — не заваливает ли рутина).
 */
export const CHRONICLE_THRESHOLD = 0.4;

/**
 * ПРИБАВКА ИЗВЕСТНОСТИ за упоминание в летописи (задача 3.2, D-068, закон №7). На КАЖДОГО
 * eid-субъекта записи Chronicle зовёт `incFame(resources, eid, FAME_PER_CHRONICLE)` — каузальный
 * шаг «тебя внесли в летопись → ты известнее» (закон №2, из состояния мира, а не «X% славы»). Это
 * ЗАПУСКАЕТ обратную связь §10.2: у кого больше записей — тем выше `fame`, а значит выше значимость
 * его БУДУЩИХ событий (лифт significance по fame, D-067), особенно смерти («пал легендарный сталкер»).
 * При `5` и `FAME_CAP=100` полный разгон известности требует ~20 упоминаний — легенды растут не
 * мгновенно. Стартовое значение; тюнингует balance-analyst (быстрее/медленнее раскрутка славы).
 */
export const FAME_PER_CHRONICLE = 5;

// ── Радио (Radio 3.5, D-070): порог эфира + погода-глушилка ────────────────────

/**
 * ПОРОГ ЗНАЧИМОСТИ РАДИО-ЭФИРА (задача 3.5, D-070, закон №7). Событие прошлого тика
 * озвучивается в эфир (`radio/message`), только если `significance(ev, world) >=
 * RADIO_THRESHOLD` И у события есть живой наблюдатель И в контенте есть пул шаблонов на
 * этот тип. Порог НИЖЕ летописного (`CHRONICLE_THRESHOLD = 0.4`): эфир БОЛТЛИВЕЕ летописи —
 * сталкеры в рацию комментируют и то, что не тянет на «строку в хронике». При `0.2`
 * озвучиваются РОВНО 7 narrative-worthy типов, у которых заведён пул `messages.json` (D-069):
 * прибытие новичка (base 0.22), подбор артефакта/грабёж (0.28), завязка стычки (0.30),
 * разрешение боя (0.42+), смерть NPC (0.48+), гибель поселения (1.0). Рутина ниже порога молчит:
 * торговля (0.06), восприятие (0.02), появление трупа (0.06), смена погоды (0.10), смерть зверя
 * (0.10). Порог ПРИЧИНЕН (закон №2): выводится из состояния мира, без rng. Стартовое значение;
 * тюнингует balance-analyst по «интересности эфира» (GDD §8.1: 15 минут чтения ×60 без пустот и
 * без свалки шума).
 */
export const RADIO_THRESHOLD = 0.2;

/**
 * ПОГОДА-ГЛУШИЛКА эфира (задача 3.5, D-070, §8.1, закон №7). Пока текущая погода среды
 * (`WorldClock.weather`) входит в этот набор, радио-сообщения ТЕРЯЮТСЯ (не эмитятся) —
 * помехи глушат передачу. `'storm'` — гроза: электрическая активность забивает диапазон
 * (в GDD помехи от грозы/выброса; выброс/emission станет отдельным погодным кодом в Фазе 3
 * и добавится сюда). Список — коды `WeatherType` из `balance/weather` (закон №10 — не
 * дублируем перечень; Radio маппит их в `WEATHER_CODE`). Потеря ДЕТЕРМИНИРОВАНА из состояния
 * Weather (не rng, закон №8): один и тот же мир на том же тике теряет те же сообщения.
 * ПОЛИТИКА «потеря, а не искажение»: искажение услышанного — механика Rumors 3.6
 * (ретрансляция с ошибкой); первичный эфир под грозой просто НЕ проходит (проще, честнее для
 * закона №1 — «тишина под грозой» сама по себе сообщение).
 */
export const RADIO_JAMMING_WEATHER: readonly string[] = ['storm'];

// ── Известность (fame): аккумулятор и его влияние на значимость ────────────────

/**
 * Потолок известности (`fame`). Аккумулятор `incFame` монотонно растёт до этого предела и
 * дальше не поднимается — иначе одна «легенда Зоны» переклампила бы шкалу значимости в 1.0
 * для всех своих событий. Нормировка `fame / FAME_CAP ∈ [0..1]` — множитель влияния ниже.
 */
export const FAME_CAP = 100;

/**
 * ВЛИЯНИЕ известности на значимость ∈ [0..1]: какую долю «оставшегося запаса до 1.0»
 * добирает событие с МАКСИМАЛЬНО известным участником. Формула значимости лифтит базовый
 * вес `base` к единице: `base + (1 − base) · fameNorm · FAME_INFLUENCE`. При `0.8` смерть
 * анонима (base 0.48, fameNorm 0) остаётся 0.48, а смерть легенды (fameNorm 1) поднимается
 * до `0.48 + 0.52·0.8 ≈ 0.90`. Так «смерть NPC = вес известности» (GDD §10.2) выполняется
 * непрерывно, а не ступенькой.
 */
export const FAME_INFLUENCE = 0.8;

