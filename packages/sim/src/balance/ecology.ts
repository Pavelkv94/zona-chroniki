// множитель по коду. Значения СТАРТОВЫЕ; тюнингует balance-analyst по метрикам прогона.

/** Погодный множитель выхода собирательства по типу погоды (0..1). */
const FORAGE_WEATHER_FACTOR_BY_TYPE: Readonly<Record<WeatherType, number>> = {
  clear: 1.0,
  overcast: 1.0,
  rain: 0.5,
  fog: 0.65,
  storm: 0.3,
};

/**
 * Погодный множитель выхода собирательства, ИНДЕКСИРОВАННЫЙ КОДОМ погоды
 * (`WorldClock.weather` = индекс в WEATHER_TYPES). Собран из
 * `FORAGE_WEATHER_FACTOR_BY_TYPE` через порядок WEATHER_TYPES (единый источник кодов,
 * закон №10 — код оперирует индексом, семантика в данных). TaskEffects читает
 * `weather` (ui8) и берёт `FORAGE_WEATHER_FACTOR_BY_CODE[weather]` (по умолчанию 1.0,
 * если код вне диапазона — среды нет/будущая погода). Детерминировано (закон №8).
 */
export const FORAGE_WEATHER_FACTOR_BY_CODE: readonly number[] = WEATHER_TYPES.map(
  (w) => FORAGE_WEATHER_FACTOR_BY_TYPE[w],
);

// ── Личная охотничья память → ЭКОНОМИЧЕСКАЯ недоохота (P-5/Б, D-087) ──────────
//
// ⚠️ АНТИ-ЧИТ (главный фильтр Б): охотник оперирует ТОЛЬКО ЛИЧНО ВОСПРИНЯТЫМ и
// СОБСТВЕННОЙ ПАМЯТЬЮ — никакого мирового счётчика вида, скана всех животных, чужих
// результатов. Он ПОМНИТ свои недавние наблюдения по угодьям: где ЛИЧНО видел живую
// дичь (contacts Perception 1.7) и где сходил ВПУСТУЮ (стоял у угодья, дичи нет). Это
// «ожидаемая добыча» угодья в ЕГО голове (expectation 0..1). Перевыбитое угодье, куда
// он раз за разом приходит и не находит дичи, ЗАБЫВАЕТСЯ (expectation падает ниже
// порога) ⇒ он перестаёт туда ходить ⇒ угодье успевает расплодиться БЕЗ мирового
// знания: саморегуляция ЭМЕРДЖЕНТНА из личного опыта. Хранение/семантика — см.
// systems/hunt-memory.ts. Здесь только ставки/пороги (закон №7). Значения СТАРТОВЫЕ.

/**
 * Убыль «ожидаемой добычи» угодья за один ПУСТОЙ выход-тик (охотник СТОИТ у угодья
 * из своей памяти, но НЕ воспринимает там живой дичи). Несколько бесплодных тиков у
 * угодья эродируют веру ниже порога ⇒ угодье забывается. Причинно из ЛИЧНО наблюдённого
 * (нет дичи в contacts), не таймер/шанс (закон №2). Стартовое, тюнит balance-analyst.
 */
export const HUNT_GROUND_EMPTY_PENALTY = 0.1;

/**
 * Порог «ожидаемой добычи»: угодье с expectation < этого забывается (запись снимается)
 * ⇒ выпадает из кандидатов-угодий охотника (HUNT туда недоступна, пока снова не увидит
 * там дичь). Перевыбитое угодье уходит из ЕГО карты — экономическая недоохота.
 */
export const HUNT_GROUND_MIN_EXPECTATION = 0.2;

/**
 * Максимальный возраст записи охотничьей памяти (тиков) без подкрепления наблюдением:
 * старше — угодье забывается (страховка от «вечной» памяти об угодье, куда он давно не
 * возвращался; ~30 дней). Детерминированная функция (tick − записанный tick), не таймер.
 * Долгая память угодий — чтобы охота СОХРАНЯЛА СМЫСЛ: охотник, узнавший угодье, помнит
 * его достаточно долго, чтобы вернуться, когда проголодается (иначе редкие ранние
 * наблюдения выветривались бы до того, как пригодятся — охота падала бы в ноль).
 */
export const HUNT_GROUND_MAX_AGE_TICKS = 43200;

// ── Зомби / реанимация (нежить, закон №2, лорные правила) ────────────────────

/**
 * Задержка (тиков) от гибели по лорным правилам до реанимации трупа в зомби (система
 * 5.7). Зомби пополняются ТОЛЬКО из погибших (закон №3: источник — конкретный труп),
 * не спавнятся из воздуха. Итоговую каденцию системы реанимации и её кратность задаст
 * задача 5.7 (значение 120 = 2 часа; guard появится вместе с системой).
 */
export const REANIMATION_DELAY_TICKS = 120;

/**
 * Радиус (в переходах-рёбрах графа карты) слышимости ШУМА для зомби с moveDriver
 * 'noise': бой/гроза порождают событие-шум, зомби в пределах этого радиуса тянутся к
 * источнику. Влияние шума — ЧЕРЕЗ события (закон №6), не прямой вызов между системами.
 */
export const ZOMBIE_NOISE_RADIUS = 2;

// ── Guard-канарейка кратности длительностей фаз (закон №8) ────────────────────
//
// Длительности фаз выброса ОБЯЗАНЫ быть кратны каденции системы эмиссии — иначе смена
// фазы пришлась бы между запусками и молча не случилась бы (как gestationTicks кратен
// ANIMALS_CADENCE в animals.ts). Проверяем при загрузке модуля и падаем ГРОМКО.
for (const [name, ticks] of [
  ['EMISSION_IMMINENT_TICKS', EMISSION_IMMINENT_TICKS],
  ['EMISSION_ACTIVE_TICKS', EMISSION_ACTIVE_TICKS],
] as const) {
  if (ticks % EMISSION_CADENCE !== 0) {
    throw new Error(
      `balance/ecology: ${name} (${ticks}) должен быть кратен EMISSION_CADENCE ` +
        `(${EMISSION_CADENCE}) — иначе смена фазы выброса не попадёт на due-тик системы ` +
        `эмиссии и молча проскочит. Правьте константу или каденцию.`,
    );
  }
}
