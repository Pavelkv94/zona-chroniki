/**
 * @module @zona/sim/balance/ecology
 *
 * Балансовые константы экологии — пастьба, питьё и ПРИЧИННОЕ размножение животных
 * (закон №7: ставки/пороги живут ТОЛЬКО здесь, а не магическими числами в системе
 * Animals 1.9). Значения СТАРТОВЫЕ; их тюнингует balance-analyst по метрикам
 * длинного headless-прогона.
 *
 * ── Единицы и связь с Needs (1.5) ────────────────────────────────────────────
 * Нужды животного — та же шкала 0..100, что у людей (0 = сыт/напоён, 100 = предел),
 * и растит их ТА ЖЕ система Needs (1.5) каждый тик (HUNGER_PER_TICK/THIRST_PER_TICK).
 * Animals (1.9) работает с шагом `every:30`, поэтому за один свой вызов ОБЯЗАН
 * компенсировать НАКОПЛЕННЫЕ за 30 тиков нужды — ставки ниже задаются «за тик» и в
 * системе домножаются на длительность шага (как «×every:30» в контракте). Так
 * животное в кормном/водном угодье держит нужды у нуля и не вымирает (главный тест
 * закона №1: экология живёт без игрока), а в бедном/безводном — голодает/жаждет и
 * эмерджентно гибнет через урон истощения Needs (мортальность реальна, а не спавн).
 *
 * ── Размножение — ПОРОГИ СОСТОЯНИЯ, не «X% приплод» (закон №2) ────────────────
 * Рождение возникает из СОСТОЯНИЯ мира: размер локальной популяции < `reproCap`
 * (species.json), корм локации > `REPRO_FORAGE_MIN`, в локации стада >=
 * `REPRO_MIN_HERD_IN_LOC` взрослых (родители ФИЗИЧЕСКИ существуют, закон №3).
 * Периодичность («племенной тик») — детерминированная функция (tick, herd,
 * gestationTicks), НЕ бросок кости и БЕЗ хранимого таймера (resume-safe, закон №8).
 * Здесь — только ПОРОГИ; сама формула периодичности живёт в системе (structural).
 */

// ── Пастьба и питьё (ставки «за тик», домножаются на длительность шага 1.9) ───

/**
 * Базовая ставка восстановления голода за тик ПАСТЬБЫ. Фактическое восстановление
 * за один вызов Animals = `ANIMAL_GRAZE_HUNGER_PER_TICK * loc.forage * every`
 * (обилие подножного корма 0..1 масштабирует: в богатых угодьях животное наедается,
 * в бедных — почти нет). Подобрано так, чтобы в типичном wild-угодье (forage ~0.4–0.7)
 * восстановление обгоняло накопление голода Needs (HUNGER_PER_TICK=0.035), а в
 * мёртвой зоне (forage ~0.05) — нет (голод копится → эмерджентная гибель). Корм —
 * из СРЕДЫ локации (закон №3: не из воздуха), в инвентарь не кладётся.
 */
export const ANIMAL_GRAZE_HUNGER_PER_TICK = 0.09;

/**
 * Ставка восстановления жажды за тик ПИТЬЯ у воды. Восстановление за вызов Animals =
 * `ANIMAL_DRINK_THIRST_PER_TICK * every` ТОЛЬКО если `loc.water === true` (питьё из
 * реки/колодца локации — из СРЕДЫ, закон №3). Не масштабируется обилием (вода
 * бинарна). Заведомо обгоняет накопление жажды Needs (THIRST_PER_TICK=0.07), поэтому
 * у воды животное не обезвоживается; в безводной локации жажда копится → гибель.
 */
export const ANIMAL_DRINK_THIRST_PER_TICK = 0.15;

// ── Размножение: пороги СОСТОЯНИЯ (закон №2 — причинность, не шанс) ───────────

/**
 * Минимальное обилие подножного корма локации (`loc.forage`, 0..1), при котором в
 * ней возможно рождение. Ниже порога стадо не плодится (голодная земля не кормит
 * потомство) — это причинный тормоз размножения из состояния мира, а не «X% шанс».
 * Мёртвые зоны (Саркофаг forage 0.05) отсекаются; wild/ruins-угодья (0.3–0.7) —
 * проходят.
 */
export const REPRO_FORAGE_MIN = 0.15;

/**
 * Минимум взрослых особей стада В ЛОКАЦИИ рождения, чтобы состоялся приплод.
 * Родитель обязан ФИЗИЧЕСКИ существовать (закон №3: новорождённый рождён стадом, не
 * из воздуха); одиночка не размножается. Порог 2 = нужна пара (реалистично и
 * гарантирует наличие родителей). Одно рождение на стадо за племенной тик.
 */
export const REPRO_MIN_HERD_IN_LOC = 2;

/**
 * Стартовое значение КАЖДОЙ нужды новорождённого (hunger/thirst/fatigue). СТРОГО
 * ниже критических порогов (D-027): новорождённый не должен стартовать на пороге —
 * иначе Needs (1.5) не поймает пересечение вверх и урон истощения пошёл бы без
 * события-причины (разрыв причинной летописи, закон №6). Малое значение = сытый,
 * напоённый, отдохнувший детёныш при рождении.
 */
export const ANIMAL_NEWBORN_NEED = 10;

// ── Артефакты: рождение из аномального поля (задача 2.9, закон №2/№7, D-054) ───
//
// Артефакт РОЖДАЕТСЯ ПРИЧИННО, не «X% выпадения» (закон №2): заряд поля
// `AnomalyField.charge` копится ДЕТЕРМИНИРОВАННО каждый тик (интринсик-физика
// аномалии — категория «генерация среды», как накопление длительности погоды D-028
// или рост нужд Needs), и при достижении порога поле «разряжается» в физический
// предмет (наземный лут поля), а заряд списывается на стоимость артефакта. Всё —
// чистая арифметика над состоянием поля, БЕЗ rng и БЕЗ хранимого таймера
// (resume-safe: `charge` — сериализуемое SoA-поле, D-046/1.0; аккумулятор сам себе
// «часы»). Значения СТАРТОВЫЕ; тюнингует balance-analyst по метрикам длинного прогона.

/**
 * Прирост заряда аномального поля ЗА ТИК. Система ArtifactSpawn (`every:N`) за один
 * свой вызов добавляет `ARTIFACT_CHARGE_PER_TICK * N` (компенсация редкого шага, как
 * ставки пастьбы «×every» выше). Заряд нормирован ~0..1 (см. AnomalyField.charge):
 * поле наполняется от 0 к порогу и разряжается. Подобрано так, чтобы наполнение до
 * порога занимало десятки часов игрового времени (артефакты редки и ценны), а не
 * секунды. Строго меньше порога за один шаг (guard-канарейка в системе): иначе поле
 * копило бы заряд быстрее, чем разряжается (не более одного артефакта за вызов).
 */
export const ARTIFACT_CHARGE_PER_TICK = 0.0005;

/**
 * Порог заряда, при котором поле рождает ОДИН артефакт, после чего заряд списывается
 * на эту же величину (стоимость артефакта). Остаток заряда сверх порога переносится
 * на следующий цикл (не теряется). Значение 1.0 = поле должно «зарядиться полностью»
 * (шкала charge ~0..1). Порог — из состояния мира (закон №2), а не «шанс».
 */
export const ARTIFACT_SPAWN_THRESHOLD = 1.0;
