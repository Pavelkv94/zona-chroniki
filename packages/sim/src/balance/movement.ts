/**
 * Множитель длительности перехода ночью: ночью двигаются медленнее/осторожнее.
 * Итоговые тики = ceil(edge.len × NIGHT_SPEED_PENALTY) при движении в ночь.
 */
export const NIGHT_SPEED_PENALTY = 1.35;

/**
 * Множитель длительности при высокой усталости (fatigue выше критического
 * порога, см. needs). Изнурённый идёт дольше.
 */
export const FATIGUE_SPEED_PENALTY = 1.25;

/** Минимальная длительность любого перехода в тиках (защита от 0). */
export const MIN_TRAVEL_TICKS = 1;
