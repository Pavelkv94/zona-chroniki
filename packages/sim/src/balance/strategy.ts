/**
 * @module @zona/sim/balance/strategy
 *
 * Балансовые константы СТРАТЕГИЧЕСКОГО AI фракций (Фаза 5, задача 5.1, закон №7:
 * веса/пороги живут ТОЛЬКО здесь, не магическими числами в системе стратегии 5.9).
 * Фракция раз в стратегический такт ОЦЕНИВАЕТ обстановку (карта угроз/ресурсов/
 * отношений) и выбирает СТРАТЕГИЧЕСКОЕ действие: зачистка мутантов (SWEEP), налёт на
 * фракцию (RAID) или дипломатия (переговоры/союз). Выбор — детерминированный argmax
 * взвешенной оценки из СОСТОЯНИЯ мира (закон №2), а не «X% шанс войны».
 *
 * ── Причинность (закон №1/№2) ────────────────────────────────────────────────
 * Оценки складываются из наблюдаемого состояния: `threat` — плотность мутантов/
 * выбросов у территории; `resourceDeficit` — нехватка ресурсов поселений фракции;
 * `relationHostility` — враждебность отношений (getRelation<0); `allyStrength` —
 * численность/сила союзников. Диспозиция фракции (`FactionData.stance`, data) смещает
 * приоритет (crusader-Долг взвешивает SWEEP выше). Значения СТАРТОВЫЕ; тюнит
 * balance-analyst по метрикам длинного headless-прогона (мир живёт без игрока).
 */

/**
 * Веса драйверов стратегической оценки (аналог utility.W для NPC, но на уровне
 * фракции). Каждый драйвер ОБЯЗАН иметь числовой вес — пропуск ключа даёт NaN в
 * argmax (как в utility.W). Порядок/набор — контракт с системой стратегии (5.9).
 */
export const W_STRAT = {
  /** Угроза мутантов/выбросов у территории фракции — тянет к SWEEP (зачистке). */
  threat: 0.5,
  /** Нехватка ресурсов поселений фракции — тянет к RAID (добыть силой) или торговле. */
  resourceDeficit: 0.45,
  /** Враждебность отношений (getRelation<0) — тянет к RAID против врага. */
  relationHostility: 0.4,
  /** Сила союзников — тянет к дипломатии (закрепить союз) и остужает риск в одиночку. */
  allyStrength: 0.3,
} as const;

/**
 * Порог оценки, выше которого фракция инициирует ЗАЧИСТКУ мутантов (SWEEP): выводит
 * бойцов выбивать псевдособак/зомби/кровососов у своей территории. Из состояния карты
 * угроз (закон №2), не «шанс похода».
 */
export const SWEEP_THRESHOLD = 0.6;

/**
 * Порог оценки, выше которого фракция инициирует НАЛЁТ (RAID) на другую фракцию:
 * враждебность + дефицит перевешивают риск. Держим ВЫШЕ SWEEP-порога (война с людьми
 * дороже зачистки зверья и требует более острого повода).
 */
export const RAID_THRESHOLD = 0.7;

/**
 * Порог оценки, выше которого фракция идёт на ДИПЛОМАТИЮ (переговоры/союз/торговое
 * соглашение) вместо силы: когда сила союзника и общий враг перевешивают вражду.
 * Ниже RAID-порога — дипломатия дешевле войны и выбирается при меньшем накале.
 */
export const DIPLOMACY_THRESHOLD = 0.5;
