/**
 * @module @zona/shared/schedule
 *
 * Контракт расписания систем для планировщика тиков (реализация — задача 0.2).
 *
 * Система исполняется на тике `t`, когда `(t - phase) % every === 0`. Поле
 * `phase` сдвигает фазу, чтобы «тяжёлые» системы с одинаковым периодом не
 * приходились на один и тот же тик. Здесь объявлен только контракт-тип; сам
 * планировщик и порядок исполнения — в `core/scheduler.ts` (D-005, D-006).
 *
 * Пример:
 * ```ts
 * const schedule: SystemSchedule = { every: 10, phase: 3 };
 * // исполнится на тиках 3, 13, 23, ...
 * ```
 */

/** Периодичность запуска системы: каждые `every` тиков со сдвигом `phase`. */
export interface SystemSchedule {
  /** Период в тиках. `every: 1` — каждый тик. Должно быть >= 1. */
  readonly every: number;
  /** Сдвиг фазы в тиках. По умолчанию 0. Условие запуска: `(t-phase)%every===0`. */
  readonly phase?: number;
}

/** Человекочитаемое уникальное имя системы (используется для rng.fork, D-004). */
export type SystemName = string;
